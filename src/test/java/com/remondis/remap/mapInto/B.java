package com.remondis.remap.mapInto;

public class B {
  private String name;
  private int age;
  private Integer size;

  public B() {
    super();
  }

  public B(String name, int age, Integer size) {
    super();
    this.name = name;
    this.age = age;
    this.size = size;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public Integer getSize() {
    return size;
  }

  public void setSize(Integer size) {
    this.size = size;
  }

}
